package com.nbcc.entities;

/**
 *
 * @author billy
 */
public abstract class Person {
    private String eye_color;
    private String hair_color;
    private String height;
    private int age;
    private String gender;
    private String hair_length;
    private String hair_style;
    private String freckles;
    

    public String getEye_color() {
        return eye_color;
    }

    public void setEye_color(String eye_color) {
        this.eye_color = eye_color;
    }

    public String getHair_color() {
        return hair_color;
    }

    public void setHair_color(String hair_color) {
        this.hair_color = hair_color;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getHair_length() {
        return hair_length;
    }

    public void setHair_length(String hair_length) {
        this.hair_length = hair_length;
    }

    public String getHair_style() {
        return hair_style;
    }

    public void setHair_style(String hair_style) {
        this.hair_style = hair_style;
    }

    public String getFreckles() {
        return freckles;
    }

    public void setFreckles(String freckles) {
        this.freckles = freckles;
    }
    
    

    
    
}
